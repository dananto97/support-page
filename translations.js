function updateLanguage(lang) {
    // Salva la preferenza
    localStorage.setItem('preferredLang', lang);

    // Aggiorna il selettore
    const selector = document.getElementById('lang-selector');
    if(selector) selector.value = lang;

    // Aggiorna tutti i testi con attributo data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Gestione Placeholder per input
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            }
            // Se contiene HTML (come <br> o <li>), usa innerHTML, altrimenti textContent
            else if (translations[lang][key].includes('<')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
}

// All'avvio della pagina
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'it';
    updateLanguage(savedLang);

    const selector = document.getElementById('lang-selector');
    if (selector) {
        selector.addEventListener('change', (e) => {
            updateLanguage(e.target.value);
        });
    }
});
